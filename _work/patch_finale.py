# -*- coding: utf-8 -*-
# Патч finale.snbt: иконка на каждый квест + переформулировка концовки как
# "Часть I — Первая Печать" (это только начало борьбы, не финал).
import io, sys

P = 'config/ftbquests/quests/chapters/finale.snbt'
t = open(P, encoding='utf-8').read()
orig = t

ICONS = {
    "70A0000000000001": "minecraft:heart_of_the_sea",          # Голос из глубин
    "70A0000000000004": "minecraft:nether_star",               # Звезда мрака
    "70A0000000000007": "minecraft:netherite_sword",           # Клинок без страха
    "70A000000000000A": "immersive_armors:divine_chestplate",  # Доспех богов
    "70A000000000000D": "immersive_armors:divine_helmet",      # Полное облачение
    "70A0000000000012": "minecraft:enchanted_golden_apple",    # Эликсиры конца
    "70A0000000000016": "minecraft:wither_skeleton_skull",     # Слуги тьмы
    "70A000000000001A": "minecraft:nether_star",               # Сила для печати
    "70A000000000001D": "minecraft:end_crystal",               # Ритуал пробуждения
    "70A0000000000020": "minecraft:wither_rose",               # Повелительница Бездны
    "70A0000000000023": "minecraft:nether_star",               # Запечатать тьму
    "70A00000000000FF": "minecraft:elytra",                    # финал главы
}

for qid, icon in ICONS.items():
    anchor = '\n\t\t\tid: "%s"\n' % qid
    if ('icon: "%s"\n\t\t\tid: "%s"' % (icon, qid)) in t:
        continue  # уже есть
    assert anchor in t, "не найден quest id " + qid
    t = t.replace(anchor, '\n\t\t\ticon: "%s"%s' % (icon, anchor), 1)

# --- переформулировки (это только начало) ---
REPL = [
    ('"Время пришло. Бездна почти проснулась — и только ты можешь её остановить."',
     '"Время пришло. Бездна почти проснулась — и только ты встанешь у неё на пути. Это первая битва из многих."'),
    ('"Это последний бой. Победи её — или сгинь во льдах навсегда."',
     '"Победи её — или сгинь во льдах. Но даже её смерть не закроет Бездну навсегда."'),
    ('"Вонзи звезду в сердце круга и захлопни врата навеки."',
     '"Вонзи звезду в сердце круга и захлопни первый разлом. Это удержит Бездну — на время."'),
    ('"Лёд стихает. Тьма уходит. И ты наконец вспоминаешь, кто ты есть."',
     '"Лёд стихает, первый разлом закрыт. Но под печатью всё ещё бьётся древнее сердце — терпеливо, медленно."'),
    ('"Мир спасён твоей рукой. Расправь крылья — путь окончен. Ты победил."',
     '"Ты выиграл первую битву, но не войну. Расправь крылья и переведи дух: Бездна лишь приоткрыла глаз. Часть II ждёт впереди."'),
    ('title: "Бездна запечатана"', 'title: "Первая печать легла"'),
    ('\ttitle: "Финал — Печать Бездны"', '\ttitle: "Часть I — Первая Печать"'),
]
for a, b in REPL:
    if a not in t:
        print("WARN: не найдена строка для замены:", a[:50])
    t = t.replace(a, b)

if t == orig:
    print("нет изменений?!"); sys.exit(1)
open(P, 'w', encoding='utf-8').write(t)
print("finale.snbt пропатчен. иконок:", len(ICONS), "| braces:", t.count('{'), t.count('}'))
